#include <stdio.h>

int main() {
    int a = 10; // integer
    float b = 12.3; // float
    double c = 12.3456789; // double
    char d = 'A'; // character
    char str[] = "Hello World!"; // string

    printf("Integer: %d\n", a);
    printf("Float: %f\n", b);
    printf("Double: %lf\n", c);
    printf("Character: %c\n", d);
    printf("String: %s\n", str);

    return 0;
}